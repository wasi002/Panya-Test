import UIKit

extension UIColor {
    
    class var yellowPColor: UIColor {
        return UIColor(red: 247/255, green: 210/255, blue: 0/255, alpha: 1.0)
    }
    
    class var darkPColor: UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 0.4)
    }
    
    class var gradientTopColor: UIColor {
        return UIColor(red: 253/255, green: 74/255, blue: 60/255, alpha: 1.0)
    }
    
    class var gradientBottomColor: UIColor {
        return UIColor(red: 255/255, green: 51/255, blue: 123/255, alpha: 1.0)
    }
}
